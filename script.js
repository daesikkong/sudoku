let currentDifficulty = 'easy';
let currentPuzzleIndex = null;
let errors = 0;
let numSelected = null;
let tiles = [];
let startTime = null;
let timerInterval = null;
let hintsUsed = 0;
let isDarkMode = false;

// 최고 기록 저장/불러오기
function saveRecord(difficulty, time, errors, hints) {
    const record = { time, errors, hints, date: new Date().toISOString() };
    const records = JSON.parse(localStorage.getItem('sudokuRecords') || '{}');
    records[difficulty] = record;
    localStorage.setItem('sudokuRecords', JSON.stringify(records));
}

function loadRecord(difficulty) {
    const records = JSON.parse(localStorage.getItem('sudokuRecords') || '{}');
    return records[difficulty] || null;
}

function loadTheme() {
    isDarkMode = localStorage.getItem('sudokuTheme') === 'dark';
    updateTheme();
}

function saveTheme() {
    localStorage.setItem('sudokuTheme', isDarkMode ? 'dark' : 'light');
}

function toggleTheme() {
    isDarkMode = !isDarkMode;
    updateTheme();
    saveTheme();
}

function updateTheme() {
    document.body.classList.toggle('dark-mode', isDarkMode);
    document.getElementById('themeToggle').textContent = isDarkMode ? '☀️' : '🌙';
}

function getRandomPuzzleIndex(difficulty, excludeIndex = null) {
    const puzzles = puzzleData[difficulty];
    if (puzzles.length === 0) return null;
    if (puzzles.length === 1) return 0;

    let idx;
    do {
        idx = Math.floor(Math.random() * puzzles.length);
    } while (idx === excludeIndex);

    return idx;
}

function formatTime(seconds) {
    const mins = Math.floor(seconds / 60);
    const secs = seconds % 60;
    return `${mins.toString().padStart(2, '0')}:${secs.toString().padStart(2, '0')}`;
}

function startTimer() {
    if (timerInterval) clearInterval(timerInterval);
    startTime = Date.now();
    timerInterval = setInterval(() => {
        const elapsed = Math.floor((Date.now() - startTime) / 1000);
        document.getElementById('timer').textContent = formatTime(elapsed);
    }, 1000);
}

function stopTimer() {
    if (timerInterval) {
        clearInterval(timerInterval);
        timerInterval = null;
    }
}

function init() {
    if (currentPuzzleIndex === null) {
        currentPuzzleIndex = getRandomPuzzleIndex(currentDifficulty);
    }

    errors = 0;
    numSelected = null;
    tiles = [];
    hintsUsed = 0;
    document.getElementById('errorCount').textContent = '0';
    document.getElementById('hintCount').textContent = '0';
    document.getElementById('winModal').classList.remove('show');
    document.getElementById('difficultyDisplay').textContent = getDifficultyText(currentDifficulty);
    updateDifficultyButtons();
    updateRecordsDisplay();
    setupTiles();
    setupButtons();
    startTimer();
}

function updateDifficultyButtons() {
    document.querySelectorAll('.difficulty-btn').forEach(btn => {
        btn.classList.remove('selected');
    });
    document.querySelector(`.${currentDifficulty}-btn`).classList.add('selected');
}

function getDifficultyText(difficulty) {
    const texts = {
        easy: '쉬움',
        medium: '중간',
        hard: '어려움'
    };
    return texts[difficulty] || difficulty;
}

function setupTiles() {
    const board = document.getElementById('board');
    board.innerHTML = '';

    const currentPuzzleData = puzzleData[currentDifficulty][currentPuzzleIndex];

    for (let r = 0; r < 9; r++) {
        for (let c = 0; c < 9; c++) {
            const tile = document.createElement('div');
            tile.className = 'tile';
            tile.dataset.row = r;
            tile.dataset.col = c;

            const char = currentPuzzleData.puzzle[r][c];
            if (char !== '-') {
                tile.textContent = char;
                tile.classList.add('fixed');
            }

            tile.addEventListener('click', () => handleTileClick(r, c, tile));
            board.appendChild(tile);
            tiles.push({ element: tile, row: r, col: c, filled: char !== '-' });
        }
    }
}

function setupButtons() {
    const container = document.getElementById('numButtons');
    container.innerHTML = '';

    for (let i = 1; i <= 9; i++) {
        const btn = document.createElement('button');
        btn.className = 'num-btn';
        btn.textContent = i;
        btn.addEventListener('click', () => selectNumber(btn, i));
        container.appendChild(btn);
    }
}

function selectNumber(btn, num) {
    if (numSelected) {
        numSelected.classList.remove('selected');
    }
    numSelected = btn;
    numSelected.classList.add('selected');
}

function handleTileClick(r, c, tile) {
    if (tile.classList.contains('fixed') || tile.textContent) {
        return;
    }

    if (!numSelected) {
        alert('먼저 숫자를 선택해주세요!');
        return;
    }

    const selectedNum = numSelected.textContent;
    const currentPuzzleData = puzzleData[currentDifficulty][currentPuzzleIndex];
    const correct = currentPuzzleData.solution[r][c];

    if (selectedNum === correct) {
        tile.textContent = selectedNum;
        tile.classList.add('filled');
        checkWin();
    } else {
        errors++;
        document.getElementById('errorCount').textContent = errors;
        tile.classList.add('selected');
        setTimeout(() => {
            tile.classList.remove('selected');
        }, 300);
    }
}

function checkWin() {
    const currentPuzzleData = puzzleData[currentDifficulty][currentPuzzleIndex];
    const allFilled = tiles.every(t => t.element.textContent !== '');
    const allCorrect = tiles.every(t => {
        if (t.element.textContent === '') return false;
        return t.element.textContent === currentPuzzleData.solution[t.row][t.col];
    });

    if (allFilled && allCorrect) {
        stopTimer();
        const elapsed = Math.floor((Date.now() - startTime) / 1000);
        saveRecord(currentDifficulty, elapsed, errors, hintsUsed);
        updateRecordsDisplay();
        document.getElementById('winModal').classList.add('show');
    }
}

function useHint() {
    if (hintsUsed >= 3) {
        alert('힌트는 게임당 최대 3번까지 사용할 수 있습니다!');
        return;
    }

    const emptyTiles = tiles.filter(t => !t.element.textContent && !t.element.classList.contains('fixed'));
    if (emptyTiles.length === 0) {
        alert('채워야 할 빈 칸이 없습니다!');
        return;
    }

    const randomTile = emptyTiles[Math.floor(Math.random() * emptyTiles.length)];
    const currentPuzzleData = puzzleData[currentDifficulty][currentPuzzleIndex];
    const correctNum = currentPuzzleData.solution[randomTile.row][randomTile.col];

    randomTile.element.textContent = correctNum;
    randomTile.element.classList.add('hinted');
    hintsUsed++;
    document.getElementById('hintCount').textContent = hintsUsed;

    // 힌트 사용 애니메이션
    randomTile.element.style.animation = 'hintPulse 0.5s ease';
    setTimeout(() => {
        randomTile.element.style.animation = '';
    }, 500);

    checkWin();
}

function updateRecordsDisplay() {
    const records = JSON.parse(localStorage.getItem('sudokuRecords') || '{}');
    const recordDisplay = document.getElementById('recordsDisplay');

    let html = '<h3>🏆 최고 기록</h3>';
    ['easy', 'medium', 'hard'].forEach(diff => {
        const record = records[diff];
        const diffText = getDifficultyText(diff);
        if (record) {
            html += `<div class="record-item">
                <span class="record-difficulty">${diffText}</span>
                <span class="record-time">${formatTime(record.time)}</span>
                <span class="record-errors">에러: ${record.errors}</span>
                <span class="record-hints">힌트: ${record.hints}</span>
            </div>`;
        } else {
            html += `<div class="record-item">
                <span class="record-difficulty">${diffText}</span>
                <span class="record-time">--:--</span>
                <span class="record-errors">에러: -</span>
                <span class="record-hints">힌트: -</span>
            </div>`;
        }
    });

    recordDisplay.innerHTML = html;
}

function resetGame() {
    currentPuzzleIndex = getRandomPuzzleIndex(currentDifficulty, currentPuzzleIndex);
    stopTimer();
    init();
}

function nextPuzzle() {
    const nextIndex = getRandomPuzzleIndex(currentDifficulty, currentPuzzleIndex);
    currentPuzzleIndex = nextIndex;
    stopTimer();
    init();
}

function changeDifficulty(difficulty) {
    if (currentDifficulty !== difficulty) {
        currentDifficulty = difficulty;
        currentPuzzleIndex = null;
        stopTimer();
        init();
    }
}

loadTheme();
init();
